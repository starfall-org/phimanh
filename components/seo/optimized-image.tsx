// Utility function to generate optimized image URLs
export function getOptimizedImageUrl(
  originalUrl: string, 
  width?: number, 
  height?: number,
  quality: number = 85
): string {
  // If using external image service, add optimization parameters
  if (originalUrl.includes('phimimg.com')) {
    const url = new URL(originalUrl);
    if (width) url.searchParams.set('w', width.toString());
    if (height) url.searchParams.set('h', height.toString());
    url.searchParams.set('q', quality.toString());
    url.searchParams.set('f', 'webp');
    return url.toString();
  }
  
  return originalUrl;
}

// Movie poster component with SEO optimizations
interface MoviePosterProps {
  movie: {
    name: string;
    poster_url: string;
    thumb_url?: string;
  };
  width?: number;
  height?: number;
  priority?: boolean;
  className?: string;
}

export function MoviePoster({ 
  movie, 
  width = 300, 
  height = 450, 
  priority = false,
  className = ''
}: MoviePosterProps) {
  const optimizedSrc = getOptimizedImageUrl(movie.poster_url, width, height);
  
  return (
    <OptimizedImage
      src={optimizedSrc}
      alt={`Poster phim ${movie.name}`}
      width={width}
      height={height}
      priority={priority}
      className={className}
      placeholder="blur"
      sizes="(max-width: 640px) 150px, (max-width: 1024px) 200px, 300px"
    />
  );
}